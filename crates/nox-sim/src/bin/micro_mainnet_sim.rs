#[derive(Parser, Debug)]
#[command(
    author,
    version,
    about = "Micro-Mainnet Simulation - With Full Mixnet Integration"
)]
struct Args {
    /// Number of mix nodes (Entry + Mix + Exit)
    #[arg(short, long, default_value_t = 5)]
    nodes: usize,

    /// Anvil fork URL (mainnet)
    #[arg(long, default_value = "https://eth.llamarpc.com")]
    fork_url: String,

    /// Use local clean slate (no fork)
    #[arg(long, default_value_t = true)]
    local: bool,

    /// Verbose output
    #[arg(short, long, default_value_t = false)]
    verbose: bool,

    /// Path to contract artifacts (generated by scripts/bootstrap-artifacts.sh)
    #[arg(long, default_value = "artifacts")]
    artifacts_path: String,

    /// Average mixnet delay per hop in milliseconds
    #[arg(long, default_value_t = 5.0)]
    delay: f64,

    /// Proof of Work difficulty (0 = disabled)
    #[arg(long, default_value_t = 0)]
    pow_difficulty: u32,
}

