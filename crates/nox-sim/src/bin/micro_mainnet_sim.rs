    let alice_bal = alice.balance(token);
    let bob_bal = bob.balance(token);

    info!("┌─────────────────────────────────────────────────────────────────────┐");
    info!("│  FINAL PRIVATE BALANCES                                            │");
    info!("├─────────────────────────────────────────────────────────────────────┤");
