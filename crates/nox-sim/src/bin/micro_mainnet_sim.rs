    info!("");
    info!("╔═══════════════════════════════════════════════════════════════╗");
    info!("║  PHASE 3: SPLIT (Alice: 7 -> 4 + 3) - PAID VIA MIXNET          ║");
    info!("║  Uses gas_payment circuit for anonymous relayer payment       ║");
    info!("║  Note 1 (7 tokens) for action, Note 2 (6) for gas reserve      ║");
    info!("╚═══════════════════════════════════════════════════════════════╝");

