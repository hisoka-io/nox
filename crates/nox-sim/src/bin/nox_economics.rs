#[derive(Subcommand, Debug)]
enum Command {
    /// Per-circuit gas consumption and proof generation time (Tier 5.1.1 + 3.2.4 + 3.2.7).
    /// Deploys contracts to Anvil, runs all 7 circuit types, measures `gas_used` and proof time.
    GasProfile {
        /// Path to contract artifacts (generated by scripts/bootstrap-artifacts.sh)
        #[arg(long, default_value = "artifacts")]
        artifacts_path: String,

        /// Number of runs per circuit for averaging
        #[arg(long, default_value_t = 1)]
        runs: usize,
    },

    /// Full `DeFi` pipeline E2E timing breakdown (Tier 3.2.1-3.2.3 + 3.2.8).
    /// Measures proof gen -> Sphinx -> mixnet transit -> chain exec for each operation.
    DefiPipeline {
        /// Path to contract artifacts (generated by scripts/bootstrap-artifacts.sh)
        #[arg(long, default_value = "artifacts")]
        artifacts_path: String,

        /// Average mixnet delay per hop in milliseconds
        #[arg(long, default_value_t = 1.0)]
        delay_ms: f64,
    },

    /// Economics analysis: USD costs, margins, break-even (Tier 5.1.2-5.1.5 + 3.2.5-3.2.6).
    /// Reads gas profile data and computes economics at various price points.
    Economics {
        /// Path to `gas_profile.json` (from gas-profile subcommand output)
        #[arg(long)]
        gas_profile_path: String,

        /// Comma-separated ETH prices in USD
        #[arg(long, default_value = "2000,3000,5000")]
        eth_prices: String,

        /// Comma-separated gas prices in gwei
        #[arg(long, default_value = "1,10,50")]
        gas_prices_gwei: String,

        /// Relayer premium in basis points (12% = 1200)
        #[arg(long, default_value_t = 1200)]
        premium_bps: u64,

        /// Monthly VPS cost in USD for break-even analysis
        #[arg(long, default_value_t = 50.0)]
        vps_cost_usd: f64,
    },

    /// Operational metrics: startup time, memory, disk I/O, mesh join (Tier 5.2.1-5.2.6).
    Operational {
        /// Comma-separated node counts for memory/startup tests
        #[arg(long, default_value = "3,5,10")]
        node_counts: String,

        /// Packets per second for load tests
        #[arg(long, default_value = "100,500,1000,5000")]
        load_rates: String,

        /// Duration of each load test in seconds
        #[arg(long, default_value_t = 10)]
        duration_secs: u64,
    },
}

